<h2>KITA SEMUA TERKESAN </h2>

<p>Bantut <i>(stunting)</i> ialah apabila kanak-kanak pendek bagi umur mereka.</p>
<p>Tetapi, mengapa pula ketinggian seorang kanak-kanak menjadi masalah kepada yang lain?</p>

<p>Bantut adalah penanda ukur yang sering digunakan untuk mengesan masalah malnutrisi kronik dalam kalangan kanak-kanak. Selain pendek, malnutrisi kronik boleh menyebabkan kanak-kanak mengalami masalah kesihatan sepanjang hidup.</p>
<p>Semua masalah ini akhirnya akan memberi kesan kepada sosioekonomi negara, sekali gus memberi impak kepada seluruh rakyat.</p>
<p>Pelbagai kajian menunjukkan kanak-kanak bantut mempunyai fungsi kognitif lemah dan mengalami kelewatan perkembangan <i>(developmental delay)</i>. Mereka juga mengalami masalah pembelajaran terutama sekali dalam matematik, membaca dan pembangunan kosa kata reseptif, demikian menurut penyelidik Derek Kok dalam laporannya Stunting in Malaysia: <i>Costs, Causes & Courses For Action</i>.</p>
<p>Ini kerana kanak-kanak bantut gagal mendapat nutrisi secukupnya ketika tempoh kritikal 1,000 hari pertama kehidupan (yang mencakupi tempoh dalam kandungan sehingga usia dua tahun). Perkembangan otak dan tubuh badan amat sensitif terhadap gangguan nutrisi ketika tempoh ini.</p>
<p>Harus diingat, gangguan nutrisi tidak semestinya disebabkan kegagalan mendapatkan bekalan makanan sihat dan mencukupi.</p>
<p>Persekitaran kurang ideal seperti ketiadaan akses air bersih dan perkhidmatan kesihatan baik juga boleh menyebabkan ibu dan anak mudah kehilangan zat apabila terkena penyakit atau jangkitan.</p>
<p>Dapatan kajian juga menunjukkan apabila dewasa, <i>individu bantut dibayar gaji 20 peratus lebih rendah</i> berbanding mereka yang tidak bantut, kata Kok kepada Bernama.</p>
<p>Menurut Tinjauan Kesihatan dan Morbiditi 2019 (NHMS 2019), 21.8 peratus kanak-kanak di Malaysia bantut.</p>
<p>Ini bererti satu daripada lima kanak-kanak di Malaysia terbantut.</p>
<p>Bayangkan satu generasi rakyat dewasa yang mana satu per lima daripadanya mengalami fungsi kognitif lemah sehingga menjejaskan kemampuan mereka mendapat pekerjaan dan pendapatan baik.</p>
<p>"Inilah mengapa masalah bantut boleh menjejaskan pasaran kerja dan pembangunan ekonomi negara," kata Kok yang juga penganalisis penyelidikan di Jeffrey Cheah Institute on Southeast Asia dan The Jeffrey Sachs Center on Sustainable Development.</p>
<p>Negara yang mengalami masalah bantut secara purata kerugian 7 peratus pendapatan per kapita, menurut satu kajian World Bank oleh Galasso and Wagstaff.</p>
<p>Kajian lain menunjukkan individu bantut mempunyai tahap imuniti yang rendah, jadi mereka lebih mudah mendapat jangkitan. Ketika dewasa pula, mereka cenderung menjadi obes, mendapat penyakit darah tinggi, kardiovaskular dan kencing manis.</p>
<p>Ini sudah tentu akan membebankan lagi sistem kesihatan awam negara.</p>
<p>Malah, kajian mendapati kesan bantut boleh diwarisi generasi berikutnya. Wanita yang bantut ketika kecil berisiko tinggi melahirkan anak bantut, menyebabkan masalah itu berulang-ulang.</p>


<h2>BANTUT DI MALAYSIA</h2>

<p>Setakat 2019, satu daripada setiap lima kanak-kanak di Malaysia terbantut.</p>
<p>Jumlah ini melibatkan sekitar 560,000 kanak-kanak – lebih dua kali ganda jumlah populasi Perlis dan lebih besar daripada populasi negara Maldives.</p>
<p>Kadar bantut kanak-kanak seluruh dunia sebenarnya semakin menurun sejak 20 tahun lalu. Namun situasi di Malaysia agak mengecewakan kerana kadarnya semakin meningkat sejak 2010.</p>
