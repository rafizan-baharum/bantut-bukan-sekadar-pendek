<h2>SEMUANYA SAMA TERKESAN</h2>

<p>Mungkin ada yang berfikiran masalah bantut ini masalah orang miskin.</p>
<p>Namun, data NHMS 2019 menunjukkan kadar bantut kanak-kanak daripada keluarga dengan pendapatan isi rumah lebih RM10,000 sebulan tetap tinggi (9.1 peratus).</p>
<p>Kadar ini masih lebih tinggi berbanding kadar penanda aras negara berpendapatan sederhana tinggi iaitu 6.0 peratus. Ini menunjukkan masalah bantut merentasi semua peringkat pendapatan.</p>

<p><iframe class="flourish-embed-iframe" frameborder="0" sandbox="allow-same-origin allow-forms allow-scripts allow-downloads allow-popups allow-popups-to-escape-sandbox allow-top-navigation-by-user-activation" scrolling="no" src="https://flo.uri.sh/visualisation/8805551/embed" style="width:100%;height:600px;" title="Interactive or visual content"></iframe></p>

<p>Kadar bantut juga tinggi dalam kalangan semua kumpulan etnik. Prevalens paling rendah dalam kalangan kaum Cina, tetapi pada kadar 14.1 peratus, ianya tetap tinggi.</p>

<p>Walaupun kadar bantut menurun apabila tahap pendidikan ibu meningkat, kadar bantut kanak-kanak dalam kalangan ibu yang mendapat pendidikan lanjut masih tinggi (18.6 peratus).</p>
<p>Status pekerjaan si ibu kelihatan mempengaruhi kadar bantut, di mana kadarnya 38.3 peratus dalam kalangan kanak-kanak yang ibunya tiada pekerjaan. Namun, prevalen bantut tetap tinggi walaupun ibu bekerja, sama ada di sektor swasta (19.4 peratus) dan sektor awam (21.7 peratus). </p>

<p><iframe class="flourish-embed-iframe" frameborder="0" sandbox="allow-same-origin allow-forms allow-scripts allow-downloads allow-popups allow-popups-to-escape-sandbox allow-top-navigation-by-user-activation" scrolling="no" src="https://flo.uri.sh/visualisation/8788510/embed" style="width:100%;height:600px;" title="Interactive or visual content"></iframe></p>

<p>Himpunan data jelas menunjukkan masalah bantut berlaku di seluruh Malaysia, merentasi rakyat pelbagai latar belakang dan pada tahap serius.</p>
<p>Kita harus akui, masalah malnutrisi di Malaysia amat membimbangkan. Jika isu ini gagal ditangani  dengan segera dan berkesan, kita jugalah yang bakal menanggung akibatnya.</p>

