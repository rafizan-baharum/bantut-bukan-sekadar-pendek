<p class="text-muted"><i>Satu daripada lima kanak-kanak di Malaysia terbantut – dan rakyat Malaysia perlu cakna. Masalah ini memberi kesan kepada semua.</i></p>

<p><i>"Apa masalahnya kalau anak saya pendek sikit? Biasalah tu, anak orang Malaysia. Takkan nak tinggi macam anak Mat Saleh."</i></p>
<p>Komen yang mungkin tidak asing lagi dan sering juga kita dengar ketinggian anak "ikut keturunan".</p>
<p>Inilah juga salah tanggapan yang biasa dicanang terutamanya dalam kalangan orang Asia - tidak mengapa kalau anak pendek sedikit, sudah begitu bakanya!</p>
<p>Namun, satu kajian yang dijalankan selama enam tahun menujukkan sebaliknya. Rupa-rupanya, <b>semua kanak-kanak di dunia mempunyai kemampuan mencapai ketinggian sama dalam lima tahun pertama kehidupan</b>, tidak kira bangsa atau keturunan.</p>
<p>Ini bererti sehingga usia lima tahun, semua kanak-kanak serata dunia – tanpa mengira latar etnik atau genetik – mampu berdiri sama tinggi.</p>
<p>Kajian mendapati yang lebih utama ialah nutrisi cukup, persekitaran sihat dan akses kepada perkhidmatan kesihatan.</p>
<p>Bagaimana pun, harus kita fahami bahawa masalah kanak-kanak bantut ini bukan sekadar kelihatan pendek berbanding rakan.</p>
<p>Kajian menunjukkan kanak-kanak bantut mengalami masalah kognitif serius hingga menjurus kepada keciciran akademik. Apabila dewasa, mereka didapati lebih sukar mendapat pekerjaan baik dan senang mendapat penyakit.</p>
<p>Mungkin ada yang membalas: "Jadi, apa masalahnya? Beri sahajalah nutrisi mencukupi sampai mereka jadi tinggi."</p>
<p>Malangnya, ia tidak semudah itu.</p>

<p>Jika masalah bantut tidak dikesan dan dibetulkan dalam tempoh 1,000 hari pertama kehidupan (sejak dalam kandungan sehingga usia dua tahun), impaknya kekal seumur hidup, demikian menurut Pertubuhan Kesihatan Sedunia (WHO).</p>
<p>Namun, untuk memahami betapa seriusnya implikasi ke atas negara dan rakyat Malaysia, kita perlu melihat nilai kerugian yang dialami negara akibat masalah bantut ini.</p>

