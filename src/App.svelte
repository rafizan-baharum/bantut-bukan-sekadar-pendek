<p>Data World Bank 2018 menunjukkan kadar bantut di Malaysia bagi  kanak-kanak bawah empat tahun pada 2016 (20.7 peratus) jauh lebih tinggi daripada Gaza pada 2014 (7.4 peratus).</p>
<p>Prevalens bantut di Malaysia juga lebih tinggi daripada negara berpendapatan sederhana yang disenaraikan dalam data seperti Ghana (18.8 peratus), Mexico (12.4 peratus), Kazakhstan (8 peratus) dan Macedonia (4.9 peratus).</p>

